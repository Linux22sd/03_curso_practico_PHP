 * SSG Renderizado se hace desde que construimos nuestra aplicacion en tiempo
 * de desarrollo. Es comun verlo cuando trabajamos con frameworks como Next.js o Nuxt.js
 * Todo el HTML es construido en tiempo de build es decir, desde que desarrollamos, lo que 
 * hace que no sea necesario un servidor.
 * 
 * Server Side Rendering
 * SSR Renderizado mas comun en la web, este renderizado se basa en que es nuestro
 * servidor quien procesa todo el codigo y manda todo el codigo ya procesado a nuestro 
 * navegador, es el mas comun en lenguajes de backend.
 * 
 * Cuando conbinamos php y html es obligatorio cerrar la etiqueta de php
 * y su extension debe de ser php
